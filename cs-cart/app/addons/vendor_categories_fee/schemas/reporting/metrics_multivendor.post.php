/** @var array $schema */

$schema['vendor_categories_fee'] = function () {
    $cnt = (int) db_get_field('SELECT COUNT(DISTINCT(category_id)) FROM ?:vendor_categories_fee WHERE percent_fee > 0');

    return $cnt > 1;
};

return $schema;